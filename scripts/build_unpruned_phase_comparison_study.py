def read_csv(path: Path) -> List[Dict[str, str]]:
    with path.open(newline="") as handle:
        return list(csv.DictReader(handle))


def write_csv(path: Path, rows: List[Dict]) -> None:
    if not rows:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)


def ensure_dirs(paths: Iterable[Path]) -> None:
    for path in paths:
        path.mkdir(parents=True, exist_ok=True)


def active_share_vector(metric_row: Dict[str, str]) -> Tuple[float, float, float]:
    active = np.array([int(metric_row[f"active_{layer_idx}"]) for layer_idx in range(3)], dtype=float)
    total_active = max(float(active.sum()), 1.0)
    shares = active / total_active
    return float(shares[0]), float(shares[1]), float(shares[2])


def share_reallocation_magnitude(
    best_shares: Tuple[float, float, float], final_shares: Tuple[float, float, float]
) -> float:
    deltas = np.abs(np.array(final_shares, dtype=float) - np.array(best_shares, dtype=float))
    return 0.5 * float(deltas.sum())


def collect_rows(alignment_csv: Path) -> List[Dict]:
    rows: List[Dict] = []
    for row in read_csv(alignment_csv):
        phase = row["prune_only_phase"]
        if phase not in PHASES:
            continue
        metrics = read_csv(Path(row["fixed_run"]) / "metrics.csv")
        if not metrics:
            continue

        test_losses = np.array([float(metric["test_loss"]) for metric in metrics], dtype=float)
        train_losses = np.array([float(metric["train_loss"]) for metric in metrics], dtype=float)
        val_losses = np.array([float(metric["val_loss"]) for metric in metrics], dtype=float)
        best_idx = int(np.argmin(val_losses))
        best = metrics[best_idx]
        final = metrics[-1]
        best_shares = active_share_vector(best)
        final_shares = active_share_vector(final)
        final_epoch = int(final["epoch"])
        best_epoch = int(best["epoch"])
        post_best_epochs = final_epoch - best_epoch

        rows.append(
            {
                "phase": phase,
                "seed": int(row["seed"]),
                "run_path": row["fixed_run"],
                "paired_prune_run": row["prune_only_run"],
                "best_epoch": best_epoch,
                "final_epoch": final_epoch,
                "post_best_epochs": post_best_epochs,
                "selected_test_loss": float(test_losses[best_idx]),
                "final_test_loss": float(test_losses[-1]),
                "final_minus_selected": float(test_losses[-1] - test_losses[best_idx]),
                "selected_train_loss": float(train_losses[best_idx]),
                "final_train_loss": float(train_losses[-1]),
                "selected_test_train_gap": float(test_losses[best_idx] - train_losses[best_idx]),
                "final_test_train_gap": float(test_losses[-1] - train_losses[-1]),
                "active_0_best": int(best["active_0"]),
                "active_1_best": int(best["active_1"]),
                "active_2_best": int(best["active_2"]),
                "active_0_final": int(final["active_0"]),
                "active_1_final": int(final["active_1"]),
                "active_2_final": int(final["active_2"]),
                "active_0_drift": int(final["active_0"]) - int(best["active_0"]),
                "active_1_drift": int(final["active_1"]) - int(best["active_1"]),
                "active_2_drift": int(final["active_2"]) - int(best["active_2"]),
                "share_0_best": best_shares[0],
                "share_1_best": best_shares[1],
                "share_2_best": best_shares[2],
                "share_0_final": final_shares[0],
                "share_1_final": final_shares[1],
                "share_2_final": final_shares[2],
                "share_0_drift": final_shares[0] - best_shares[0],
                "share_1_drift": final_shares[1] - best_shares[1],
                "share_2_drift": final_shares[2] - best_shares[2],
                "post_best_share_drift": share_reallocation_magnitude(best_shares, final_shares),
                "post_best_drift_rate": share_reallocation_magnitude(best_shares, final_shares)
                / max(post_best_epochs, 1),
                "share_2_drift_rate": (final_shares[2] - best_shares[2]) / max(post_best_epochs, 1),
                "final_param_count": int(float(final["param_count"])),
            }
        )
    rows.sort(key=lambda item: (PHASES.index(item["phase"]), item["seed"]))
    return rows


def summarize_patterns(rows: Sequence[Dict]) -> List[Dict]:
    keys = [
        "selected_test_loss",
        "final_test_loss",
        "final_minus_selected",
        "best_epoch",
        "post_best_epochs",
        "final_test_train_gap",
        "active_0_best",
        "active_1_best",
        "active_2_best",
        "active_0_final",
        "active_1_final",
        "active_2_final",
        "active_0_drift",
        "active_1_drift",
        "active_2_drift",
        "share_0_best",
        "share_1_best",
        "share_2_best",
        "share_0_final",
        "share_1_final",
        "share_2_final",
        "share_0_drift",
        "share_1_drift",
        "share_2_drift",
        "post_best_share_drift",
        "post_best_drift_rate",
        "share_2_drift_rate",
        "final_param_count",
    ]
    out: List[Dict] = []
    for phase in PHASES:
        group = [row for row in rows if row["phase"] == phase]
        if not group:
            continue
        summary = {"phase": phase, "n": len(group)}
        for key in keys:
            values = [row[key] for row in group]
            summary[f"median_{key}"] = median(values)
        out.append(summary)
    return out


def build_pairwise_stats(rows: Sequence[Dict]) -> List[Dict]:
    metrics = [
        ("selected_test_loss", "selected test loss"),
        ("final_test_loss", "final test loss"),
        ("final_minus_selected", "final minus selected"),
        ("best_epoch", "best epoch"),
        ("final_test_train_gap", "final test-train gap"),
        ("active_1_best", "active layer-1 @ best"),
        ("active_2_best", "active layer-2 @ best"),
        ("active_1_drift", "active layer-1 drift"),
        ("active_2_drift", "active layer-2 drift"),
        ("share_1_drift", "share layer-1 drift"),
        ("share_2_drift", "share layer-2 drift"),
        ("post_best_drift_rate", "post-best drift rate"),
    ]
    pairs = list(itertools.combinations(PHASES, 2))
    out: List[Dict] = []
    for phase_a, phase_b in pairs:
        group_a = [row for row in rows if row["phase"] == phase_a]
        group_b = [row for row in rows if row["phase"] == phase_b]
        if not group_a or not group_b:
            continue
        for key, label in metrics:
            values_a = [row[key] for row in group_a]
            values_b = [row[key] for row in group_b]
            out.append(
                {
                    "metric": key,
                    "label": label,
                    "group_a": phase_a,
                    "group_b": phase_b,
                    "n_group_a": len(values_a),
                    "n_group_b": len(values_b),
                    "median_group_a": median(values_a),
                    "median_group_b": median(values_b),
                    "median_diff_a_minus_b": median(values_a) - median(values_b),
                }
            )
    return out


def _scatter_by_phase(ax, rows: Sequence[Dict], key: str, log_scale: bool = False) -> None:
    phases = [phase for phase in PHASES if any(row["phase"] == phase for row in rows)]
    positions = np.arange(1, len(phases) + 1)
    groups = [[row[key] for row in rows if row["phase"] == phase] for phase in phases]
    bp = ax.boxplot(groups, patch_artist=True, tick_labels=phases, showfliers=False, widths=0.58)
    for patch, phase in zip(bp["boxes"], phases):
        patch.set_facecolor(PHASE_COLORS[phase])
        patch.set_alpha(0.28)
    for pos, phase in zip(positions, phases):
        group = [row for row in rows if row["phase"] == phase]
        jitter = np.linspace(-0.1, 0.1, len(group)) if len(group) > 1 else np.array([0.0])
        for offset, row in zip(jitter, group):
            ax.scatter(
                pos + offset,
                float(row[key]),
                s=52,
                color=PHASE_COLORS[phase],
                edgecolor="white",
                linewidth=0.6,
                zorder=3,
            )
            ax.annotate(
                f"s{row['seed']}",
                (pos + offset, float(row[key])),
                xytext=(3, 3),
                textcoords="offset points",
                fontsize=7,
                color="#444444",
            )
    if log_scale:
        ax.set_yscale("log")
    ax.grid(axis="y", linestyle="--", alpha=0.3)


def plot_phase_assignments(rows: Sequence[Dict], out_path: Path) -> None:
    ordered_rows = sorted(rows, key=lambda row: row["seed"])
    fig, ax = plt.subplots(figsize=(8.8, 1.9))
    positions = np.arange(len(ordered_rows))
    for idx, row in enumerate(ordered_rows):
        ax.scatter(
            idx,
            0,
            s=300,
            marker="s",
            color=PHASE_COLORS[row["phase"]],
            edgecolor="white",
            linewidth=0.8,
        )
    ax.set_xlim(-0.6, len(ordered_rows) - 0.4)
    ax.set_ylim(-0.5, 0.5)
    ax.set_yticks([])
    ax.set_xticks(positions)
    ax.set_xticklabels([f"seed {row['seed']}" for row in ordered_rows])
    ax.set_title("Fixed runs indexed by paired prune_only phase")
    ax.legend(
        handles=[
            plt.Line2D(
                [0],
                [0],
                marker="s",
                linestyle="",
                markersize=10,
                markerfacecolor=PHASE_COLORS[phase],
                markeredgecolor="white",
                label=phase,
            )
            for phase in PHASES
            if any(row["phase"] == phase for row in rows)
        ],
        ncol=3,
        fontsize=8,
        loc="upper center",
        bbox_to_anchor=(0.5, -0.12),
    )
    fig.tight_layout()
    fig.savefig(out_path, dpi=180)
    plt.close(fig)


