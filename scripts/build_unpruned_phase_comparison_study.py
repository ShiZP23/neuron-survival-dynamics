def _trajectory_curve(metrics: Sequence[Dict[str, str]], key: str) -> np.ndarray:
    if key in {"train_loss", "val_loss", "test_loss", "param_count", "total_pruned"}:
        return np.array([float(metric[key]) for metric in metrics], dtype=float)
    if key.startswith("active_") or key.startswith("size_"):
        return np.array([float(metric[key]) for metric in metrics], dtype=float)
    raise ValueError(f"Unsupported trajectory key: {key}")


def _plot_phase_trajectory(
    rows: Sequence[Dict],
    metric_key: str,
    out_path: Path,
    title: str,
    ylabel: str,
    log_scale: bool = False,
    ylim: Optional[Tuple[float, float]] = None,
) -> None:
    fig, ax = plt.subplots(figsize=(8.2, 4.9))
    for phase in PHASES:
        group = [row for row in rows if row["phase"] == phase]
        if not group:
            continue
        curves = []
        epochs = None
        for row in group:
            metrics = read_csv(Path(row["run_path"]) / "metrics.csv")
            curves.append(_trajectory_curve(metrics, metric_key))
            if epochs is None:
                epochs = np.array([int(metric["epoch"]) for metric in metrics], dtype=int)
        curve_array = np.stack(curves, axis=0)
        q25 = np.quantile(curve_array, 0.25, axis=0)
        q50 = np.quantile(curve_array, 0.50, axis=0)
        q75 = np.quantile(curve_array, 0.75, axis=0)
        ax.plot(epochs, q50, color=PHASE_COLORS[phase], linewidth=2.0, label=phase)
        ax.fill_between(epochs, q25, q75, color=PHASE_COLORS[phase], alpha=0.18)
    if log_scale:
        ax.set_yscale("log")
    if ylim is not None:
        ax.set_ylim(*ylim)
    ax.set_xlabel("epoch")
    ax.set_ylabel(ylabel)
    ax.set_title(title)
    ax.grid(True, linestyle="--", alpha=0.3)
    ax.legend(fontsize=8)
    fig.tight_layout()
    fig.savefig(out_path, dpi=180)
    plt.close(fig)


