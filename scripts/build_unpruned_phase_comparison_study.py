def plot_active_share_dynamics(rows: Sequence[Dict], out_path: Path) -> None:
    fig, axes = plt.subplots(1, 3, figsize=(13.6, 4.6), sharex=True, sharey=True)
    for layer_idx, ax in enumerate(axes):
        for phase in PHASES:
            group = [row for row in rows if row["phase"] == phase]
            if not group:
                continue
            curves = []
            epochs = None
            for row in group:
                metrics = read_csv(Path(row["run_path"]) / "metrics.csv")
                active = np.array(
                    [
                        [
                            int(metric["active_0"]),
                            int(metric["active_1"]),
                            int(metric["active_2"]),
                        ]
                        for metric in metrics
                    ],
                    dtype=float,
                )
                total_active = np.maximum(active.sum(axis=1, keepdims=True), 1.0)
                curves.append(active[:, layer_idx] / total_active[:, 0])
                if epochs is None:
                    epochs = np.array([int(metric["epoch"]) for metric in metrics], dtype=int)
            curve_array = np.stack(curves, axis=0)
            q25 = np.quantile(curve_array, 0.25, axis=0)
            q50 = np.quantile(curve_array, 0.50, axis=0)
            q75 = np.quantile(curve_array, 0.75, axis=0)
            ax.plot(epochs, q50, color=PHASE_COLORS[phase], linewidth=2.0, label=phase)
            ax.fill_between(epochs, q25, q75, color=PHASE_COLORS[phase], alpha=0.18)
        ax.set_ylim(0.0, 1.0)
        ax.set_title(f"layer {layer_idx}")
        ax.set_xlabel("epoch")
        ax.grid(True, linestyle="--", alpha=0.3)
    axes[0].set_ylabel("active share")
    axes[2].legend(fontsize=8)
    fig.suptitle("Active share dynamics by paired phase", fontsize=14)
    fig.tight_layout()
    fig.savefig(out_path, dpi=180)
    plt.close(fig)


